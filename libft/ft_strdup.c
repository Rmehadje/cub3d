#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*result;
	int		size;

	size = 0;
	while (s1[size])
		size++;
	result = (char *)malloc(sizeof(char) * (size + 1));
	if (result == NULL)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		result[i] = s1[i];
		i++;
	}
	result[i] = '\0';
	return (result);
}
